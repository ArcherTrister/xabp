Clear-Host
Write-Host "Deleting all NODE_MODULES and TESTRESULTS folders..." -ForegroundColor Cyan
Get-ChildItem -Path . -Include node_modules, TestResults -Recurse -Directory | ForEach-Object {
    if ($_.FullName) {
        Write-Host "Deleting:" $_.FullName -ForegroundColor Yellow
        Remove-Item $_.FullName -Recurse -Force
    }
    else {
        Write-Host "Skipping:" $_.FullName -ForegroundColor Magenta
    }
}

Write-Host "NODE_MODULES and TESTRESULTS folders have been successfully deleted." -ForegroundColor Green
